-- Bu betiği Supabase SQL Editor alanına yapıştırarak tabloları oluşturabilirsiniz.

-- Tabloları temizle (Eğer zaten varsa)
DROP TABLE IF EXISTS attendance;
DROP TABLE IF EXISTS employees;
DROP TABLE IF EXISTS users;
DROP TABLE IF EXISTS locations;
DROP TABLE IF EXISTS locked_periods;
DROP TABLE IF EXISTS audit_logs;

-- 1. LOKASYONLAR TABLOSU
CREATE TABLE locations (
    id GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code TEXT UNIQUE NOT NULL,
    name TEXT NOT NULL,
    default_hours INT DEFAULT 8
);

-- 2. KULLANICILAR TABLOSU
CREATE TABLE users (
    id GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username TEXT UNIQUE NOT NULL,
    password TEXT NOT NULL, -- Düz metin veya hash şifre
    role TEXT NOT NULL CHECK (role IN ('ADMIN', 'USER')),
    location_id INT REFERENCES locations(id) ON DELETE SET NULL,
    full_name TEXT NOT NULL,
    is_active BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW())
);

-- 3. PERSONELLER TABLOSU
CREATE TABLE employees (
    id GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    sicil_no TEXT UNIQUE NOT NULL,
    ad_soyad TEXT NOT NULL,
    location_id INT REFERENCES locations(id) ON DELETE RESTRICT,
    gorevi TEXT NOT NULL,
    is_giris DATE NOT NULL,
    is_cikis DATE,
    is_active BOOLEAN DEFAULT TRUE
);

-- 4. PUANTAJ VERİLERİ TABLOSU
CREATE TABLE attendance (
    id GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    period_key TEXT NOT NULL, -- Örn: "2026-06"
    employee_id INT REFERENCES employees(id) ON DELETE CASCADE,
    day INT NOT NULL CHECK (day >= 1 AND day <= 31),
    code TEXT NOT NULL, -- Örn: "X", "H", "Y1"
    ubgt NUMERIC DEFAULT 0,
    fm NUMERIC DEFAULT 0,
    meal BOOLEAN DEFAULT FALSE,
    CONSTRAINT unique_attendance_per_day UNIQUE (period_key, employee_id, day)
);

-- 5. DÖNEM KİLİTLERİ TABLOSU
CREATE TABLE locked_periods (
    period_key TEXT PRIMARY KEY,
    is_locked BOOLEAN DEFAULT FALSE,
    locked_by TEXT,
    locked_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW())
);

-- 6. İŞLEM GÜNLÜKLERİ TABLOSU (AUDIT LOGS)
CREATE TABLE audit_logs (
    id GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_name TEXT NOT NULL,
    action TEXT NOT NULL,
    detail TEXT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW())
);

